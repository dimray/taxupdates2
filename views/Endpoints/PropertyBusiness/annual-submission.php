<p>There is no requirement to submit an
    Annual Submission if it is not needed.</p>

<p>An Annual Submission can be made for each property or self-employment business, and adjusts the figures submitted
    through the Cumulative Summary for that business. Use the Annual Submission to:</p>

<div class="list">
    <ul>
        <li>Claim the Property Income Allowance</li>
        <li>Make a Private Use Adjustment</li>
        <li>Share the RentARoom Allowance</li>
        <li>Declare you are a Non-Resident Landlord</li>
        <li>Claim for Replacement of Domestic Items</li>
        <li>Claim Capital Allowances</li>
        <li>Declare Balancing Charges</li>
    </ul>
</div>

<h3 id="amend">Amending A Submission</h3>

<p>To change an Annual Submission after it has been submitted, make a new submission. The new submission
    will
    overwrite the previous submission, so it is necessary to provide all figures as though no Annual Submission has
    previously been made.</p>

