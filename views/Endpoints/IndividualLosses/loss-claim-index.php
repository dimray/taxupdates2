    <h2>Property Business Losses</h2>



    <p>Losses from a property business are automatically carried forward and set against future profits from the same
        business, no claim is needed.</p>



    <p> In some circumstances, property losses can be set against general income. A
        standard residential property business is unlikely to be able to set losses against general income, HMRC's guidance
        can be found <a target="_hmrc"
            href="https://www.gov.uk/hmrc-internal-manuals/property-income-manual/pim4205">here</a>.</p>



    <p>If your loss meets the conditions to be set against general income, and you do not want to carry it forward, you can
        create a loss claim to carry it sideways. Claims cannot be made
        until after the end of the tax year of the loss.</p>



<?php else: ?>

    <h2>Trading Losses</h2>

    <p>Losses from self-employment can be set against other income in either the year of loss or the
        previous year, or they can be carried forward to set against future self-employment profits. In some circumstances
        losses can also be
        set against capital gains. HMRC's guidance can be found <a target="_hmrc"
            href="https://www.gov.uk/hmrc-internal-manuals/business-income-manual/bim85000">here</a>.
    </p>


    <p>If this business made a trading loss in the tax year, create a loss claim to tell HMRC
        how you will use it. Claims cannot be made
        until after the end of the tax year of the loss.</p>

