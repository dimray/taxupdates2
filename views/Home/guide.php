<h2>Final Declaration</h2>

<div class="list">
    <ul>
        <li>A running tax calculation, provided by HMRC, can be viewed at any time throughout the tax year.</li>
        <li>After the end of the tax year, a Final Declaration must be submitted. This declares that the tax
            calculation correctly shows your final tax position for the year.</li>
        <li>This site can be used to submit details of other income and gains to HMRC and also claim reliefs, so that
            all
            information
            is included in the tax calculation before the Final Declaration is submitted.</li>
        <li>There is no need to also submit a Self-Assessment tax return to HMRC. The Final Declaration closes your tax
            position
            for the year.</li>
</div>
</ul>

