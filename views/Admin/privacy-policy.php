<p>TaxUpdates respects your privacy and is committed to protecting your personal data. Only the information needed to
    provide services and enable you to access and manage your tax information
    efficiently is collected and stored.</p>

<p>Wherever practical, data is retrieved live from HMRC’s systems rather than stored by TaxUpdates, helping to minimise
    the amount of personal data retained.</p>

<p>The following information may be collected and stored:</p>

<ul class="list">
    <li>Your name</li>
    <li>Your email address</li>
    <li>Your National Insurance Number</li>
    <li>Your agent account reference (if applicable)</li>
    <li>Details of submissions you make through TaxUpdates</li>
</ul>

<p>Your national insurance number is encrypted to ensure it remains secure and protected
    against unauthorised access.</p>

<p>TaxUpdates complies with HMRC's requirements to collect <a href="https://www.legislation.gov.uk/uksi/2019/360/made"
        target="hmrc">Fraud Prevention Data</a>.
    This means limited information about your device (such as screen size, timezone, and IP address) is collected and
    included with each HMRC API request, in line with HMRC’s fraud prevention requirements.</p>


<p>You can permanently delete your account at any time in the
    Profile section. When you delete your account, all personal data held by TaxUpdates is immediately and irreversibly
    deleted.</p>

